/**
 * Get flow-specific segment dictionary with base segments merged
 * @param {object} varObj - Variable object containing customerName and customerProject
 * @returns {Map} New merged segment dictionary for the flow (cached per flow)
 */
function getSegmentDic(varObj) {
    if (!varObj || typeof varObj !== 'object') {
        Logger.warn('getSegmentDic: varObj is required', {
            hasVarObj: !!varObj,
            varObjType: typeof varObj,
        });
        return null;
    }

    var customerName = varObj.customerName || '';
    var customerProject = varObj.customerProject || '';
    var flowKey = customerName + '_' + customerProject;

    Logger.debug('getSegmentDic: varObj.customerName = "' + varObj.customerName + '"');
    Logger.debug('getSegmentDic: varObj.customerProject = "' + varObj.customerProject + '"');
    Logger.debug('getSegmentDic: Computed flowKey = "' + flowKey + '"');

    if (flowKey === '_') {
        Logger.error(
            'getSegmentDic: Empty flow key detected! This means customerName and customerProject are not set.',
            { flowKey: flowKey },
            null
        );
        Logger.error(
            'getSegmentDic: This usually indicates lineMap was not loaded or lineMap lookup failed.',
            { flowKey: flowKey },
            null
        );
        Logger.warn('getSegmentDic: Defaulting to ENGIE_ENERGYLINE flow');
        flowKey = 'ENGIE_ENERGYLINE';
    }

    // Check cache first
    if (segmentDicMergedCache[flowKey] && segmentDicMergedCache[flowKey] instanceof Map) {
        Logger.debug('getSegmentDic: Cache hit for flowKey: ' + flowKey);
        return segmentDicMergedCache[flowKey];
    }

    // Determine which flow-specific map to use
    // Currently hardcoded, waiting for db tables
    var flowMap = null;
    if (flowKey === 'ENGIE_ENERGYLINE') {
        flowMap = segmentDic_ENGIE_ENERGYLINE;
    } else if (flowKey === 'ENGIE_PROF') {
        flowMap = segmentDic_ENGIE_PROF;
    } else {
        Logger.warn(
            'getSegmentDic: Unknown flow key: ' + flowKey + ', defaulting to ENGIE_ENERGYLINE'
        );
        flowMap = segmentDic_ENGIE_ENERGYLINE;
    }

    if (!flowMap || !(flowMap instanceof Map)) {
        Logger.error(
            'getSegmentDic: Flow map not found or invalid for: ' + flowKey,
            { flowKey: flowKey },
            null
        );
        return null;
    }

    var mergedMap = new Map();

    // add all base segments
    if (segmentDic_BASE && segmentDic_BASE instanceof Map) {
        segmentDic_BASE.forEach(function (value, key) {
            mergedMap.set(key, value);
        });
    } else {
        Logger.warn('getSegmentDic: segmentDic_BASE not found or invalid', { flowKey: flowKey });
    }

    // Then, add flow-specific segments
    flowMap.forEach(function (value, key) {
        mergedMap.set(key, value);
    });

    // Cache the merged map for this flow
    segmentDicMergedCache[flowKey] = mergedMap;
    Logger.info(
        'getSegmentDic: Created merged segmentDic for ' +
            flowKey +
            ' (base: ' +
            (segmentDic_BASE ? segmentDic_BASE.size : 0) +
            ' segments, flow: ' +
            flowMap.size +
            ' segments, merged: ' +
            mergedMap.size +
            ' segments)'
    );

    return mergedMap;
}
